package io.github.poorguy.explore.learn.linkedlist;

class ListNode {
    int val;
    ListNode next;

    public ListNode() {}

    ListNode(int x) {
        val = x;
        next = null;
    }

    ListNode(int x, ListNode next) {
        val = x;
        this.next = next;
    }
}
